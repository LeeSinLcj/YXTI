export function localizeQuestion(question: Question, locale: AppLocale): Question {
  if (locale === 'zh-CN') return question

  const translation = questionTranslations[locale]?.[question.id]
  if (!translation) return question

  return {
    ...question,
    text: translation.text,
    prompt: translation.text,
  }
}